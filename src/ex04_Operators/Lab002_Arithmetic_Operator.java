package ex04_Operators;

public class Lab002_Arithmetic_Operator {

    public static void main(String[] args) {

        // Arithmetic Operators
        //        + (Addition)
        //       - (Subtraction)
        //       * (Multiplication)
        //       / (Division)
        //       % (Modulus) | Modulus - Remainder

       int a = 20;
        int b = 3;
        float c = 3.000F;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c);
    }
}
