package ex04_Operators;

public class Lab011_Interview_Ques_Ex02
{
    public static void main(String[] args) {

        // concatination

        String first_name = "Virat";
        String lastname = "Dhoni";

        int a = 1;
        int b = 2;


        System.out.println(first_name + lastname + a + b);
        System.out.println(a + b + first_name + lastname);
        System.out.println(first_name + lastname  + "\t" + (a + b));




        // + -> behave differently with the data type.
        // + -> operator overloading
    }
}
