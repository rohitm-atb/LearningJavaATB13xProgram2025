package ex05_TypeCasting;

public class Lab007_Task_02 {

    public static void main(String[] args) {

//        Explicit Typecasting (Narrowing)
//        Description: Casts a double to an int, cutting off decimals value.

        double d = 10.04;

        int b = (int)d;

        System.out.println(b);
    }
}
