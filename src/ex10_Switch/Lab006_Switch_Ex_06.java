package ex10_Switch;

public class Lab006_Switch_Ex_06 {

    public static void main(String[] args) {

        char ch = 'C';

        switch (ch)
        {
            case 65 :
                System.out.println("Match ASCII");
                break;

            case 66 :
                System.out.println("Match ASCII");
                break;

            default:
                System.out.println("No Match");
                break;
        }

    }
}
