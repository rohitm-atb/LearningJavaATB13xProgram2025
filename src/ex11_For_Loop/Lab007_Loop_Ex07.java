package ex11_For_Loop;

public class Lab007_Loop_Ex07 {

    public static void main(String[] args) {

        for (long i = 1l; i < 10; i++) {
            System.out.println(i);
        }
        for (float f = 0.0f; f < 10.67; f++) {
            System.out.println("Hi,Float -> " + f);
        }
    }
}
