package ex11_For_Loop;

public class Lab011_Loop_Ex11 {

    public static void main(String[] args) {

        //  Find the even numbers from 1 to 50.
        for (int i = 1; i <=50; i++)
        {
            if(i%2==0)
            {
                System.out.println("Even Number : "+i);
            }

        }
    }
}
