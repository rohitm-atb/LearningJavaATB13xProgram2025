package ex17_Arrays;

public class Lab005_Array_Print_Ex04 {

    public static void main(String[] args) {

        int[] marks = {51, 72, 44, 67, 80};

        System.out.println("---- Print this Marks ----");

        for (int i = 0; i < marks.length; i++)
        {
            System.out.println(marks[i]);
        }
    }
}
